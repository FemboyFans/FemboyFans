       created_at,
       updated_at,
       up_score,
       down_score,
       score,
       source,
       mupload_media_assets.md5,
       rating,
       is_note_locked,
       is_rating_locked,
       is_status_locked,
       is_pending,
       is_flagged,
       is_deleted,
       uploader_id,
       approver_id,
       last_noted_at,
       last_comment_bumped_at,
       fav_count,
       tag_string,
       tag_count,
       tag_count_general,
       tag_count_artist,
       tag_count_character,
       tag_count_copyright,
       upload_media_assets.file_ext,
       upload_media_assets.file_size,
       upload_media_assets.image_width,
       upload_media_assets.image_height,
       parent_id,
       has_children,
       last_commented_at,
       has_active_children,
       tag_count_meta,
       tag_count_species,
       tag_count_invalid,
       description,
       change_seq,
       tag_count_lore,
       bg_color,
       upload_media_assets.duration,
       upload_media_assets.framecount,
       upload_media_assets.thumbnail_frame,
       original_tag_string,
       tag_count_contributor,
       tag_count_gender,
       qtags,
       upload_url
  FROM public.posts
  JOIN upload_media_assets ON posts.upload_media_asset_id = upload_media_assets.id
  ORDER BY id
