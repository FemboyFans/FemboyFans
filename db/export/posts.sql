SELECT posts.id,
       posts.created_at,
       posts.updated_at,
       posts.up_score,
       posts.down_score,
       posts.score,
       posts.source,
       upload_media_assets.md5,
       posts.rating,
       posts.is_note_locked,
       posts.is_rating_locked,
       posts.is_status_locked,
       posts.is_pending,
       posts.is_flagged,
       posts.is_deleted,
       posts.uploader_id,
       posts.approver_id,
       posts.last_noted_at,
       posts.last_comment_bumped_at,
       posts.fav_count,
       posts.tag_string,
       posts.tag_count,
       posts.tag_count_general,
       posts.tag_count_artist,
       posts.tag_count_character,
       posts.tag_count_copyright,
       upload_media_assets.file_ext,
       upload_media_assets.file_size,
       upload_media_assets.image_width,
       upload_media_assets.image_height,
       posts.parent_id,
       posts.has_children,
       posts.last_commented_at,
       posts.has_active_children,
       posts.tag_count_meta,
       posts.tag_count_species,
       posts.tag_count_invalid,
       posts.description,
       posts.change_seq,
       posts.tag_count_lore,
       posts.bg_color,
       upload_media_assets.duration,
       upload_media_assets.framecount,
       posts.thumbnail_frame,
       posts.original_tag_string,
       posts.tag_count_contributor,
       posts.tag_count_gender,
       posts.qtags,
       posts.upload_url
  FROM public.posts
  JOIN upload_media_assets ON posts.upload_media_asset_id = upload_media_assets.id
  ORDER BY posts.id ASC
